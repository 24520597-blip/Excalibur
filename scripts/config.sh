#!/usr/bin/env bash
# Excalibur Authentication Configuration
# Interactive setup for LLM provider authentication

set -e

# Colors
RED='\033[0;31m'
GREEN='\033[0;32m'
BLUE='\033[0;34m'
PURPLE='\033[0;35m'
YELLOW='\033[0;33m'
NC='\033[0m'

# Get the directory where this script is located, then go up to project root
SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
PROJECT_DIR="$(dirname "$SCRIPT_DIR")"
ENV_FILE="${PROJECT_DIR}/.env.auth"

echo -e "${PURPLE}"
cat << "EOF"
    Excalibur - Authentication Configuration
EOF
echo -e "${NC}"

echo -e "${BLUE}Select authentication method:${NC}\n"
echo -e "  ${GREEN}[1]${NC} Claude Code Subscription ${YELLOW}(Recommended)${NC}"
echo -e "      Use your Claude Code subscription via 'claude login'"
echo ""
echo -e "  ${GREEN}[2]${NC} OpenRouter API Key"
echo -e "      Route requests through OpenRouter to use GPT-5.1, Gemini, etc."
echo ""
echo -e "  ${GREEN}[3]${NC} Anthropic API Key"
echo -e "      Use Anthropic's Claude directly with your API key"
echo ""
echo -e "  ${GREEN}[4]${NC} Gemini API Key"
echo -e "      Use Google's Gemini API directly"
echo ""
echo -e "  ${GREEN}[5]${NC} Vertex AI Express Mode API Key"
echo -e "      Use an API key created by Vertex AI Express Mode"
echo ""
echo -e "  ${GREEN}[6]${NC} Local LLM (via LM Studio, Ollama, etc.)"
echo -e "      Route requests to a local LLM server on your host machine"
echo ""

read -p "Enter your choice [1-6] (default: 1): " choice
choice="${choice:-1}"

case $choice in
    1)
        # Save auth mode for Claude Code subscription (manual login)
        cat > "$ENV_FILE" << EOF
# Excalibur Authentication Configuration
# Generated by make config
EXCALIBUR_AUTH_MODE=manual
EOF

        echo -e "${GREEN}Claude Code subscription mode selected.${NC}"
        echo -e "${BLUE}After running 'make connect', execute 'claude login' inside the container.${NC}"
        ;;

    2)
        echo ""
        echo -e "${BLUE}OpenRouter Configuration${NC}"
        echo -e "${YELLOW}Get your API key from: https://openrouter.ai/keys${NC}"
        echo ""
        read -sp "Enter your OpenRouter API key: " openrouter_key
        echo ""

        if [ -z "$openrouter_key" ]; then
            echo -e "${RED}Error: API key cannot be empty${NC}"
            exit 1
        fi

        # Save auth mode
        cat > "$ENV_FILE" << EOF
# Excalibur Authentication Configuration
# Generated by make config
EXCALIBUR_AUTH_MODE=openrouter
OPENROUTER_API_KEY=${openrouter_key}
# You can customize your OpenRouter model here:
OPENROUTER_MODEL=deepseek/deepseek-chat
EOF

        # Set restrictive permissions on the file
        chmod 600 "$ENV_FILE"

        echo -e "${GREEN}OpenRouter configuration saved!${NC}"
        echo -e "${BLUE}CCR will be configured automatically when you run 'make connect'${NC}"
        ;;

    3)
        echo ""
        echo -e "${BLUE}Anthropic API Key Configuration${NC}"
        echo -e "${YELLOW}Get your API key from: https://console.anthropic.com/settings/keys${NC}"
        echo ""
        read -sp "Enter your Anthropic API key: " anthropic_key
        echo ""

        if [ -z "$anthropic_key" ]; then
            echo -e "${RED}Error: API key cannot be empty${NC}"
            exit 1
        fi

        # Save auth mode
        cat > "$ENV_FILE" << EOF
# Excalibur Authentication Configuration
# Generated by make config
EXCALIBUR_AUTH_MODE=anthropic
ANTHROPIC_API_KEY=${anthropic_key}
EOF

        # Set restrictive permissions on the file
        chmod 600 "$ENV_FILE"

        echo -e "${GREEN}Anthropic API key saved!${NC}"
        ;;

    4)
        echo ""
        echo -e "${BLUE}Gemini API Key Configuration${NC}"
        echo -e "${YELLOW}Get your API key from: https://aistudio.google.com/apikey${NC}"
        echo ""
        read -sp "Enter your Gemini API key: " gemini_key
        echo ""

        if [ -z "$gemini_key" ]; then
            echo -e "${RED}Error: API key cannot be empty${NC}"
            exit 1
        fi

        cat > "$ENV_FILE" << EOF
# Excalibur Authentication Configuration
# Generated by make config
EXCALIBUR_AUTH_MODE=gemini
LLM_PROVIDER=gemini
LLM_MODEL=gemini-2.5-flash
GEMINI_API_KEY=${gemini_key}
EOF

        chmod 600 "$ENV_FILE"

        echo -e "${GREEN}Gemini API configuration saved!${NC}"
        echo -e "${BLUE}Run 'make connect', then use: excalibur --target <target>${NC}"
        ;;

    5)
        echo ""
        echo -e "${BLUE}Vertex AI Express Mode API Key Configuration${NC}"
        echo -e "${YELLOW}A standard Google Cloud project API key will not work here.${NC}"
        echo -e "${YELLOW}Create an Express Mode key: https://console.cloud.google.com/vertex-ai/studio${NC}"
        echo ""
        read -sp "Enter your Vertex AI Express Mode API key: " vertex_key
        echo ""

        if [ -z "$vertex_key" ]; then
            echo -e "${RED}Error: API key cannot be empty${NC}"
            exit 1
        fi

        cat > "$ENV_FILE" << EOF
# Excalibur Authentication Configuration
# Generated by make config
EXCALIBUR_AUTH_MODE=gemini
LLM_PROVIDER=gemini
LLM_MODEL=gemini-2.5-flash
GEMINI_API_MODE=vertex
GEMINI_API_KEY=${vertex_key}
EOF

        chmod 600 "$ENV_FILE"

        echo -e "${GREEN}Vertex AI API key configuration saved!${NC}"
        ;;

    6)
        # Save auth mode for Local LLM
        cat > "$ENV_FILE" << EOF
# Excalibur Authentication Configuration
# Generated by make config
EXCALIBUR_AUTH_MODE=local
EOF

        echo -e "${GREEN}Local LLM mode selected!${NC}"
        echo ""
        echo -e "${BLUE}Setup Instructions:${NC}"
        echo "  1. Start your local LLM server (e.g., LM Studio) on your host machine"
        echo "     Default expected URL: http://localhost:1234/v1/chat/completions"
        echo ""
        echo "  2. To customize models or URL, edit:"
        echo "     scripts/ccr-config-template.json"
        echo ""
        echo "  3. Run 'make connect' to start Excalibur"
        ;;

    *)
        echo -e "${RED}Invalid choice. Exiting.${NC}"
        exit 1
        ;;
esac

echo ""
echo -e "${GREEN}Configuration complete!${NC}"
echo -e "Run ${PURPLE}make connect${NC} to start Excalibur."
